// Helper functions for filtering and searching activities
export const getActivitiesByCategory = (category: ActivityCategory): Activity[] => {
  return activities.filter(activity => activity.category === category);
};

export const getActivitiesByMood = (mood: WeekendMood): Activity[] => {
  return activities.filter(activity => activity.mood.includes(mood));
};

export const getActivitiesByTimeSlot = (timeSlot: TimeSlot): Activity[] => {
  return activities.filter(activity => activity.timeSlot.includes(timeSlot));
};

export const searchActivities = (query: string): Activity[] => {
  const lowercaseQuery = query.toLowerCase();
  return activities.filter(activity => 
    activity.title.toLowerCase().includes(lowercaseQuery) ||
    activity.description.toLowerCase().includes(lowercaseQuery) ||
    activity.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getRandomActivity = (): Activity => {
  return activities[Math.floor(Math.random() * activities.length)];
};

export const getActivityById = (id: string): Activity | undefined => {
  return activities.find(activity => activity.id === id);
};

